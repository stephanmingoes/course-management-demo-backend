import mongoose from "mongoose";

const LecturerSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
    unique: true,
  },
});

export default mongoose.model("Lecturer", LecturerSchema);
